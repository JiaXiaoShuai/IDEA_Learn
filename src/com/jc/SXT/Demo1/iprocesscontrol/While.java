package com.jc.SXT.Demo1.iprocesscontrol;

public class While {
    public static void main(String[] args) {
        int a = 0;
        while(a < 10){
            a++;
        }
        System.out.println(a);

        int b = 0;
        do{
            b++;
        }while(b<0);

        System.out.println(b);
    }
}
